        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        double fx, Dx;
        
        String choice = jCBFuzzyType.getSelectedItem().toString();
        if(choice == "One Side"){
            for(int i=1;i<chartData[0].length;i++){
                Dx = chartData[0][i]-chartData[1][i];
                //Dx = chartData[1][i]-chartData[0][i];
                if(Dx>rv)
                    fx=0;
                else
                    fx=((rv+1)-Dx)/(rv+1);
                dataset.setValue(fx, "", String.valueOf(i));
            }
        }
        else{
            for(int i=1;i<chartData[0].length;i++){
                Dx = chartData[0][i]-chartData[1][i];
                //Dx = chartData[1][i]-chartData[0][i];
                if(Dx<=rv ||Dx>=-rv)
                    fx=((rv+1)-abs(Dx))/(rv+1);
                else
                    fx=0;
                dataset.setValue(fx, "", String.valueOf(i));
            }
        }
        
        JFreeChart chart = ChartFactory.createLineChart("Fuzzy Requirement Satisfaction", "Day", "Fuzzy Satisfaction", dataset, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot p = chart.getCategoryPlot();
        p.setRangeGridlinePaint(Color.DARK_GRAY);
        ChartFrame frame = new ChartFrame("Fuzzy Representation",chart);
        frame.setVisible(true);
        frame.setSize(1000,400);

